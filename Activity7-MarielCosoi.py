# Create a function named add that expects two arguments
# Return the sum of those two arguments.
# Repeat this process for Subtracting, Multiplying and Dividing
def add(a=1,b=5):
    sum = a+b
    print("Sum")
    print(sum)

def subtract(a=1, b=5):
    subtracting = a-b
    print("Subtraction")
    print(subtracting)

def multiplication(a=1, b=5):
    multiplying = a*b
    print("Multiplication")
    print(multiplying)

def division(a=1, b=5):
    dividing = a/b
    print("Division")
    print(dividing)

add(3,8)
subtract(3,8)
multiplication(3,8)
division(3,8)


